#!/usr/bin/env node
/**
 * Phase 1 — Bulk Asset Migration to Firebase Storage
 *
 * Uploads /public/avatars/** and /public/images/reviews/** to Firebase Storage
 * under the `static/` prefix, then generates lib/staticAssets.ts with the
 * resulting public URLs.
 *
 * Prerequisites:
 *   - service-account.json placed at repo root (gitignored)
 *   - NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET set in .env.local
 *
 * Usage:
 *   node scripts/upload-public-assets.mjs
 *   node scripts/upload-public-assets.mjs --dry-run    # list files, no upload
 */

import admin from "firebase-admin";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { createRequire } from "node:module";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const require = createRequire(import.meta.url);

// --- Load .env.local manually (no dotenv dep needed) ---
const envPath = path.join(ROOT, ".env.local");
if (fs.existsSync(envPath)) {
    for (const line of fs.readFileSync(envPath, "utf8").split("\n")) {
        const m = line.match(/^([A-Z0-9_]+)=(.*)$/);
        if (m) process.env[m[1]] = m[2].replace(/^["']|["']$/g, "");
    }
}

const BUCKET = process.env.NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET;
if (!BUCKET) {
    console.error("❌ NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET not set in .env.local");
    process.exit(1);
}

const DRY_RUN = process.argv.includes("--dry-run");
const SERVICE_ACCOUNT_PATH = path.join(ROOT, "service-account.json");

let bucket = null;
if (!DRY_RUN) {
    if (!fs.existsSync(SERVICE_ACCOUNT_PATH)) {
        console.error("❌ service-account.json not found at repo root.");
        console.error("   Download from Firebase Console → Project Settings → Service accounts → Generate new private key");
        process.exit(1);
    }
    admin.initializeApp({
        credential: admin.credential.cert(require(SERVICE_ACCOUNT_PATH)),
        storageBucket: BUCKET,
    });
    bucket = admin.storage().bucket();
}

// --- Manifest of what to upload ---
// localDir: source folder under /public
// remotePrefix: destination prefix in Firebase Storage
const MIGRATIONS = [
    { localDir: "public/avatars/kids", remotePrefix: "static/avatars/kids", ext: ".png" },
    { localDir: "public/avatars/female", remotePrefix: "static/avatars/female", ext: ".png" },
    { localDir: "public/avatars/male", remotePrefix: "static/avatars/male", ext: ".png" },
    { localDir: "public/avatars/animals", remotePrefix: "static/avatars/animals", ext: ".png" },
    { localDir: "public/avatars/monsters", remotePrefix: "static/avatars/monsters", ext: ".png" },
    { localDir: "public/images/reviews", remotePrefix: "static/reviews", ext: ".jpg" },
];

/**
 * Upload a single file and make it publicly readable.
 * Returns the public URL.
 */
async function uploadFile(localPath, remotePath) {
    if (DRY_RUN) {
        return `https://storage.googleapis.com/${BUCKET}/${remotePath}`;
    }
    const [file] = await bucket.upload(localPath, {
        destination: remotePath,
        metadata: {
            cacheControl: "public, max-age=31536000, immutable",
        },
    });
    await file.makePublic();
    return `https://storage.googleapis.com/${BUCKET}/${remotePath}`;
}

// --- Run migration ---
const manifest = {}; // { "kids": ["url1", "url2", ...], ... }

console.log(DRY_RUN ? "🧪 DRY RUN (no uploads)" : "🚀 Uploading to bucket:", BUCKET);
console.log("");

for (const { localDir, remotePrefix, ext } of MIGRATIONS) {
    const absDir = path.join(ROOT, localDir);
    if (!fs.existsSync(absDir)) {
        console.warn(`⚠️  Skip: ${localDir} does not exist`);
        continue;
    }

    const files = fs
        .readdirSync(absDir)
        .filter((f) => f.toLowerCase().endsWith(ext))
        .sort((a, b) => {
            // Natural sort by trailing number (e.g. review2.jpg < review10.jpg)
            const na = parseInt(a.match(/(\d+)/)?.[1] ?? "0", 10);
            const nb = parseInt(b.match(/(\d+)/)?.[1] ?? "0", 10);
            return na - nb;
        });

    console.log(`📂 ${localDir} → ${remotePrefix}/ (${files.length} files)`);

    const categoryKey = remotePrefix.split("/").pop(); // e.g. "kids", "reviews"
    manifest[categoryKey] = [];

    for (const fname of files) {
        const localPath = path.join(absDir, fname);
        const remotePath = `${remotePrefix}/${fname}`;
        const url = await uploadFile(localPath, remotePath);
        manifest[categoryKey].push(url);
        console.log(`   ✓ ${fname}`);
    }
    console.log("");
}

// --- Generate lib/staticAssets.ts ---
const tsOut = `// AUTO-GENERATED by scripts/upload-public-assets.mjs
// DO NOT EDIT MANUALLY. Re-run the script to regenerate.
// Generated: ${new Date().toISOString()}

export const STATIC_AVATARS = {
    kids: ${JSON.stringify(manifest.kids ?? [], null, 8).replace(/\n/g, "\n    ")},
    female: ${JSON.stringify(manifest.female ?? [], null, 8).replace(/\n/g, "\n    ")},
    male: ${JSON.stringify(manifest.male ?? [], null, 8).replace(/\n/g, "\n    ")},
    animals: ${JSON.stringify(manifest.animals ?? [], null, 8).replace(/\n/g, "\n    ")},
    monsters: ${JSON.stringify(manifest.monsters ?? [], null, 8).replace(/\n/g, "\n    ")},
} as const;

export const STATIC_REVIEWS: readonly string[] = ${JSON.stringify(manifest.reviews ?? [], null, 4)};
`;

const outPath = path.join(ROOT, "lib/staticAssets.ts");
if (DRY_RUN) {
    console.log("🧪 DRY RUN — would write:", outPath);
    console.log(tsOut);
} else {
    fs.writeFileSync(outPath, tsOut, "utf8");
    console.log("✅ Wrote manifest:", outPath);
}

console.log("");
console.log("🎉 Done.");
process.exit(0);
