(async () => {
  for (const [courseId, cur] of Object.entries(CURRICULA)) {
    const ref = db.collection('courses').doc(courseId);
    const snap = await ref.get();
    const sp = snap.data().salesPage;
    if (!sp) { console.log('⚠️ no salesPage:', courseId); continue; }
    let touched = false;
    const sections = sp.sections.map((s) => {
      if (s.type !== 'curriculum') return s;
      touched = true;
      return { ...s, data: { ...s.data, title: cur.title, subtitle: cur.subtitle, chapters: cur.chapters } };
    });
    if (!touched) { console.log('⚠️ no curriculum section:', courseId); continue; }
    await ref.update({ 'salesPage.sections': sections });
    console.log(`✅ ${courseId} (${snap.data().title}) · curriculum → ${cur.chapters.length} บท (สสวท. 2560)`);
  }
  process.exit(0);
})().catch((e) => { console.error('ERROR:', e.message); process.exit(1); });
