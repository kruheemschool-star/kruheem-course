(async () => {
  const ONLY = process.argv[2]; // optional: build just one course id
  for (const [courseId, cfg] of Object.entries(CONFIGS)) {
    if (ONLY && courseId !== ONLY) continue;
    const ref = db.collection('courses').doc(courseId);
    const snap = await ref.get();
    if (!snap.exists) { console.log('⚠️ not found:', courseId); continue; }
    const existing = snap.data().salesPage;
    if (existing) {
      const file = path.resolve(__dirname, `backup-${courseId}-${Date.now()}.json`);
      fs.writeFileSync(file, JSON.stringify(existing, null, 2));
      console.log(`   (backed up existing salesPage → ${path.basename(file)})`);
    }
    const salesPage = buildSalesPage(cfg);
    await ref.update({ salesPage });
    console.log(`✅ ${cfg.grade} | ${courseId} · ${salesPage.sections.length} sections · theme:${salesPage.theme.id} · ${cfg.curriculum.length} บท`);
    if (cfg.note) console.log(`   ⚠️ note: ${cfg.note}`);
  }
  console.log('\nDone. Countdown ends:', END);
  process.exit(0);
})().catch((e) => { console.error('ERROR:', e.message); process.exit(1); });
